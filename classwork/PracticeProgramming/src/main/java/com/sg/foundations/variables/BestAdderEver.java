public class BestAdderEver {
    public static void main(String[] args) {
        // declare variables
        int num1, num2, num3;
        int result;
        
        // assign values
        num1 = 4;
        num2 = 9;
        num3 = 2;
        result = num1 + num2 + num3;
        
        // print output
        System.out.println(num1 + "+" + num2 + "+" + num3 + "=" + result);
    }
}
